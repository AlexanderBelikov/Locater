    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_login);

        Toolbar toolbar = findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);
        getSupportActionBar().setDisplayHomeAsUpEnabled(true);

        mEmailView = findViewById(R.id.email);
        mPasswordView = findViewById(R.id.password);
        mPasswordView.setOnEditorActionListener(new TextView.OnEditorActionListener() {
            @Override
            public boolean onEditorAction(TextView textView, int id, KeyEvent keyEvent) {
                if (id == EditorInfo.IME_ACTION_DONE || id == EditorInfo.IME_NULL) {
                    attemptLogin(false);
                    return true;
                }
                return false;
            }
        });

        Button mEmailSignInButton = findViewById(R.id.email_sign_in_button);
        mEmailSignInButton.setOnClickListener(new OnClickListener() {
            @Override
            public void onClick(View view) {
                attemptLogin(false);
            }
        });

        Button mEmailRegisterButton = findViewById(R.id.email_register_button);
        mEmailRegisterButton.setOnClickListener(new OnClickListener() {
            @Override
            public void onClick(View view) {
                attemptLogin(true);
            }
        });
        mAuth = FirebaseAuth.getInstance();
        mLoginFormView = findViewById(R.id.login_form);
        mProgressView = findViewById(R.id.login_progress);
    }

    private void attemptLogin(boolean isNewUser) {
        // Reset errors.
        mEmailView.setError(null);
        mPasswordView.setError(null);

        // Store values at the time of the login attempt.
        String email = mEmailView.getText().toString();
        String password = mPasswordView.getText().toString();

        boolean cancel = false;
        View focusView = null;

        // Check for a valid password, if the user entered one.
        if (TextUtils.isEmpty(password) || password.length()<6) {
            mPasswordView.setError(getString(R.string.error_invalid_password));
            focusView = mPasswordView;
            cancel = true;
        }

        // Check for a valid email address.
        if (TextUtils.isEmpty(email)) {
            mEmailView.setError(getString(R.string.error_field_required));
            focusView = mEmailView;
            cancel = true;
        }

        if (cancel) {
            // There was an error; don't attempt login and focus the first
            // form field with an error.
            focusView.requestFocus();
        } else {
            // Show a progress spinner, and kick off a background task to
            // perform the user login attempt.
            showProgress(true);
            if(isNewUser){
                mAuth.createUserWithEmailAndPassword(email, password)
                        .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                            @Override
                            public void onComplete(@NonNull Task<AuthResult> task) {
                                if(task.isSuccessful()){
                                    Toast.makeText(LoginActivity.this, "Register Successful", Toast.LENGTH_LONG).show();
                                    LocaterService.initAdmin(getApplicationContext());
                                    LocaterPreferences.setLocaterAdminUid(getApplicationContext(), FirebaseAuth.getInstance().getCurrentUser().getUid());
//                                    LocaterService.setLocationUpdates(getApplicationContext(), true);
                                    NavUtils.navigateUpFromSameTask(getParent());
                                } else {
                                    mEmailView.setError(task.getException().getMessage());
                                    mEmailView.requestFocus();
                                }
                                showProgress(false);
                            }
                        });
            } else {
                mAuth.signInWithEmailAndPassword(email, password)
                        .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                            @Override
                            public void onComplete(@NonNull Task<AuthResult> task) {
                                if(task.isSuccessful()){
                                    Toast.makeText(LoginActivity.this, "Login Successful", Toast.LENGTH_LONG).show();
//                                    LocaterService.initAdmin(getApplicationContext());
                                    LocaterPreferences.setLocaterAdminUid(getApplicationContext(), FirebaseAuth.getInstance().getCurrentUser().getUid());
//                                    LocaterService.setLocationUpdates(getApplicationContext(), true);
                                    NavUtils.navigateUpFromSameTask(getParent());
                                } else {
                                    String errorMessage = task.getException().getMessage();
                                    if(errorMessage.toLowerCase().contains("email")){
                                        mEmailView.setError(errorMessage);
                                        mEmailView.requestFocus();
                                    } else if(errorMessage.toLowerCase().contains("password")) {
                                        mPasswordView.setError(errorMessage);
                                        mPasswordView.requestFocus();
                                    } else {
                                        Toast.makeText(LoginActivity.this, errorMessage, Toast.LENGTH_LONG).show();
                                    }
                                }
                                showProgress(false);
                            }
                        });

            }
        }
    }

